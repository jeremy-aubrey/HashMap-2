import java.util.*;

/*
A function that returns the first duplicate encountered in an array of Strings.
The time complexity is O(N).

Author: Jeremy Aubrey
Date: 12/23/2021
Purpose: Practice using HashMaps for increasing efficiency
 */

public class DuplicateFinder {
	public static void main(String[] args) {
		String[] stub = {"test"};
		System.out.println(findDuplicate(stub));
	}
	
	public static String findDuplicate(String[] arr) {
		return arr[0];
	}
}
